# to check the relationship between the two integer variables.


def main():
    x = 2
    y = 8
    if x == y:
        print("EQUAL")
    elif x != y:
        print("NOT EQUAL")
    if x > y:
        print("GREATER")
    elif x < y:
        print("LESSER")
    if x >= y:
        print("GREATER OR EQUAL")
    elif x < y:
        print("LESSER OR EQUAL")


if __name__ == "__main__":
    main()
